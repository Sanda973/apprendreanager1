char	*ft_strrev(char *str)
{
	int	i;
	int	j;
	int	t;

	i = 0;
	j = 0;
	t = 0;
	while (str[i] != '\0')
		i++;
	i--;
	while (i > j)
	{
		t = str[j];
		str[j] = str[i];
		str[i] = t;
		i--;
		j++;
	}
	return (str);
}

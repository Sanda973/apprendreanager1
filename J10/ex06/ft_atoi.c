#include "usefull.h"

int	ft_atoi(char *str)
{
	int	neg;
	int	nb;

	neg = 1;
	nb = 0;
	while (*str <= 32)
		str++;
	if (*str == '-')
		neg = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + *str - '0';
		str++;
	}
	return (nb * neg);
}

int	ft_iterative_factorial(int nb)
{
	int f;

	f = nb - 1;
	if (nb > 0 && nb < 13)
	{
		while (f > 0)
		{
			nb = nb * f;
			f--;
		}
		return (nb);
	}
	else if (nb == 0)
		return (1);
	return (0);
}

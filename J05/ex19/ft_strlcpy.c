unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	int	i;

	i = 0;
	while (size - 1 > 0)
	{
		if (src[i] == '\0')
			return (i);
		dest[i] = src[i];
		i++;
	}
	return (i);
}
